import type { TinteTheme } from "@/types/tinte";

// Extract themes for theme showcase
export function extractRaysoThemeData(isDark = false) {
  return raysoPresets.map((preset, index) => {
    const colorData = isDark ? preset.dark : preset.light;
    return {
      id: `rayso-${index + 1}`,
      name: preset.name,
      colors: {
        primary: colorData.sc,
        secondary: colorData.ac_1,
        accent: colorData.pr,
        background: colorData.bg,
        foreground: colorData.tx,
      },
      createdAt: "2024-01-15",
      rawTheme: {
        light: preset.light,
        dark: preset.dark,
      } as TinteTheme,
    };
  });
}

